#Başlangıçta hızı 1.5 m/s olan bir cisim 4 saniye boyunca
# 1.9 m/s2 lik ivme ile hareket ediyor. Nihai hızını 
#bulan python kodu.

initial_velocity = 1.5 # Başlangıç hızı
acceleration = 1.9 # ivme
time = 4 # süre
final_velocity = initial_velocity + acceleration * time
print("Son hız: ", final_velocity, "m/s")
